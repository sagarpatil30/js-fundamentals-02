// methods in array

// push
// adds the value at last
const team =['cat','dog','monkey']
team.push('rat');
console.log(team);

// pop
// removes the value from last
const players =['player1','player2','player3'];
players.pop(2)
console.log(players);

// unshift adds the value in the first
const numbers =['1','2','3']
numbers.unshift('0')
console.log(numbers);

// Shift removes the value from first
const fruits=['apple','sweetpotato']
fruits.shift('apple')
console.log(fruits);